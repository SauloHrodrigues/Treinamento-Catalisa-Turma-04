package bola01;

public class Bola {
    public float circunferencia;
    public String cor;
    public String marca;
    public float velocidade;

    public Bola(float circunferencia, String cor, String marca, float velocidade) {
        this.circunferencia = circunferencia;
        this.cor = cor;
        this.marca = marca;
        this.velocidade = velocidade;
    }
}
